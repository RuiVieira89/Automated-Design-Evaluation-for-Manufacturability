"""FEA simulation of the fin-array heat sink: linear elastic + thermal diffusion.

Geometry : data/CAD_generated/heat_sink.step  (generated by CAD_heatSink.py)
Models   : Isotropic linear elastic (stress/displacement)
           Thermal diffusion with Robin convection BCs (temperature)

Face names in the STEP file and how they map to BCs
-----------------------------------------------------
mech_fixed_base  – base bottom (z=0)        → mechanical clamped + thermal base convection
mech_load_top    – fin tops (z=z_total)     → applied surface pressure
therm_conv_fins  – fin sides + channel floor → fin convection (cooling side)
end_cap          – x-direction end faces    → no BC (adiabatic symmetry)

See CAD_heatSink.py for the face-classification logic.

Optimization parameters:
    mech_load_pressure  – pressure on fin tops [N/mm²]
    h_conv_fins         – convection coefficient on fin surfaces [W/(mm²·K)]
    h_conv_base         – convection coefficient on base [W/(mm²·K)]
    T_ambient           – ambient temperature (fin cooling side) [K]
    T_base_hot          – hot-side temperature at base [K]

Units
-----
All lengths in mm.  Forces in N.  Stress in N/mm² (= MPa).
Thermal conductivity in W/(mm·K).  Heat-transfer coeff in W/(mm²·K).

Usage
-----
    model = HeatSinkFEA()                    # default parameters
    results = model()                        # run simulation
    results = model(plot=True)               # run + show interactive 3D plots

    model = HeatSinkFEA(mech_load_pressure=-2.0, T_ambient=295.0)
    results = model()
    model.plot(results)                      # plot separately
"""

from __future__ import annotations

import shutil
import sys
from dataclasses import dataclass, field
from pathlib import Path

import matplotlib
matplotlib.use("TkAgg")
import matplotlib.pyplot as plt
import numpy as np
import scipy.sparse as sp
import scipy.sparse.linalg as spla

ROOT = Path(__file__).resolve().parents[3]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

from EasyFEA import Display, ElemType, Mesher
from EasyFEA import Models
from EasyFEA.Models._utils import ModelType
from EasyFEA.Simulations import Elastic, Thermal

# ---------------------------------------------------------------------------
# STEP file paths
# EasyFEA's Mesh_Import_part requires the extension ".stp"
# ---------------------------------------------------------------------------
_STEP_FILE = ROOT / "data" / "CAD_generated" / "heat_sink.step"
_STP_FILE  = _STEP_FILE.with_suffix(".stp")

# ---------------------------------------------------------------------------
# Geometry constants  (must match the defaults in CAD_heatSink.py)
# ---------------------------------------------------------------------------
_FIN_HEIGHT  = 20.0   # mm
_BASE_HEIGHT =  5.0   # mm
_Z_BASE      = _BASE_HEIGHT
_Z_TOTAL     = _BASE_HEIGHT + _FIN_HEIGHT

# ---------------------------------------------------------------------------
# Material properties — Aluminium (N, mm, K unit system)
# ---------------------------------------------------------------------------
_YOUNG_E = 70_000.0   # N/mm²    (= 70 GPa)
_POISSON = 0.33
_COND_K  = 0.200      # W/(mm·K) (= 200 W/(m·K))


# ---------------------------------------------------------------------------
# Results container
# ---------------------------------------------------------------------------
@dataclass
class HeatSinkResults:
    """Scalar outputs from the FEA simulation."""
    max_stress: float        # N/mm²  – maximum von Mises stress
    max_displacement: float  # mm     – maximum displacement norm
    min_temperature: float   # K
    max_temperature: float   # K
    delta_T: float           # K      – max_temperature - T_ambient

    # Internal simulation objects kept for optional plotting
    _simu_mech:  object       = field(default=None, repr=False)
    _simu_therm: object       = field(default=None, repr=False)
    _svm:        np.ndarray   = field(default=None, repr=False)
    _u_norm:     np.ndarray   = field(default=None, repr=False)
    _T_field:    np.ndarray   = field(default=None, repr=False)
    _params:     dict         = field(default_factory=dict, repr=False)


# ---------------------------------------------------------------------------
# Internal helper
# ---------------------------------------------------------------------------
def _build_robin_matrices(
    coords: np.ndarray,
    connect: np.ndarray,
    node_mask: np.ndarray,
    Nn: int,
) -> tuple[sp.csr_matrix, np.ndarray]:
    """Build sparse surface mass matrix and load vector for a Robin BC."""
    face_local = np.array([[0, 1, 2], [0, 1, 3], [0, 2, 3], [1, 2, 3]])
    all_faces = connect[:, face_local].reshape(-1, 3)
    all_faces_sorted = np.sort(all_faces, axis=1)

    face_keys = (all_faces_sorted[:, 0] * (Nn ** 2)
                 + all_faces_sorted[:, 1] * Nn
                 + all_faces_sorted[:, 2])
    unique_keys, inv, counts = np.unique(face_keys, return_inverse=True, return_counts=True)
    boundary_face_ids = np.where(counts == 1)[0]
    boundary_mask_per_face = np.isin(inv, boundary_face_ids)
    boundary_faces = all_faces[boundary_mask_per_face]

    on_surface = node_mask[boundary_faces]
    surf_faces = boundary_faces[on_surface.all(axis=1)]

    if surf_faces.shape[0] == 0:
        return sp.csr_matrix((Nn, Nn)), np.zeros(Nn)

    p0, p1, p2 = coords[surf_faces[:, 0]], coords[surf_faces[:, 1]], coords[surf_faces[:, 2]]
    areas = 0.5 * np.linalg.norm(np.cross(p1 - p0, p2 - p0), axis=1)

    rows, cols, vals = [], [], []
    for li in range(3):
        for lj in range(3):
            rows.append(surf_faces[:, li])
            cols.append(surf_faces[:, lj])
            vals.append(areas / 12.0 * (2.0 if li == lj else 1.0))

    M_surf = sp.csr_matrix(
        (np.concatenate(vals), (np.concatenate(rows), np.concatenate(cols))),
        shape=(Nn, Nn),
    )

    f_surf = np.zeros(Nn)
    for li in range(3):
        np.add.at(f_surf, surf_faces[:, li], areas / 3.0)

    return M_surf, f_surf


# ---------------------------------------------------------------------------
# Main class
# ---------------------------------------------------------------------------
class HeatSinkFEA:
    """Callable FEA model for the fin-array heat sink.

    Parameters
    ----------
    mech_load_pressure : float
        Surface pressure on fin tops [N/mm²]. Negative = compressive.
    h_conv_fins : float
        Convection coefficient on fin surfaces [W/(mm²·K)].
    h_conv_base : float
        Convection coefficient on base [W/(mm²·K)].
    T_ambient : float
        Ambient (coolant) temperature [K].
    T_base_hot : float
        Hot-side temperature applied at the base Robin BC [K].
    mesh_size : float
        Target element edge length [mm].
    elem_type : ElemType
        Element type (TETRA4 or TETRA10).
    coord_tol : float
        Tolerance for surface node selection [mm].
    """

    def __init__(
        self,
        mech_load_pressure: float = -1.0,
        h_conv_fins: float = 5.0e-5,
        h_conv_base: float = 1.0e-4,
        T_ambient: float = 300.0,
        T_base_hot: float = 350.0,
        mesh_size: float = 3.0,
        elem_type: ElemType = ElemType.TETRA4,
        coord_tol: float = 0.5,
    ) -> None:
        self.mech_load_pressure = mech_load_pressure
        self.h_conv_fins = h_conv_fins
        self.h_conv_base = h_conv_base
        self.T_ambient = T_ambient
        self.T_base_hot = T_base_hot
        self.mesh_size = mesh_size
        self.elem_type = elem_type
        self.coord_tol = coord_tol

    def __call__(self, plot: bool = False) -> HeatSinkResults:
        """Run the simulation and return results.

        Parameters
        ----------
        plot : bool
            If True, open an interactive 3D plot after solving.
        """
        results = self._run()
        if plot:
            self.plot(results)
        return results

    # ------------------------------------------------------------------
    def _run(self) -> HeatSinkResults:
        tol = self.coord_tol

        # 1. Mesh
        print("Meshing …")
        shutil.copy(_STEP_FILE, _STP_FILE)
        mesher = Mesher()
        mesh = mesher.Mesh_Import_part(
            str(_STP_FILE), dim=3, meshSize=self.mesh_size, elemType=self.elem_type
        )
        print(f"  Nodes: {mesh.Nn}   Elements: {mesh.Ne}")
        coords = mesh.coord

        # 2. Node sets
        nodes_fixed = mesh.Nodes_Conditions(lambda x, y, z: z <= tol)
        nodes_top   = mesh.Nodes_Conditions(lambda x, y, z: z >= _Z_TOTAL - tol)
        nodes_fins  = mesh.Nodes_Conditions(
            lambda x, y, z: (np.abs(z - _Z_BASE) < tol) | (z > _Z_BASE - tol)
        )

        # 3. Mechanical simulation
        print("Running mechanical simulation …")
        mech_model = Models.Elastic.Isotropic(dim=3, E=_YOUNG_E, v=_POISSON)
        simu_mech  = Elastic(mesh, mech_model, verbosity=False)
        simu_mech.add_dirichlet(nodes_fixed, [0, 0, 0], ["x", "y", "z"])
        simu_mech.add_surfLoad(nodes_top, [self.mech_load_pressure], ["z"])
        simu_mech.Solve()

        u_x    = simu_mech.Result("ux",  nodeValues=True)
        u_y    = simu_mech.Result("uy",  nodeValues=True)
        u_z    = simu_mech.Result("uz",  nodeValues=True)
        u_norm = np.sqrt(u_x**2 + u_y**2 + u_z**2)
        svm    = simu_mech.Result("Svm", nodeValues=True)

        print(f"  Max von Mises stress : {np.max(svm):.3f} N/mm²")
        print(f"  Max displacement     : {np.max(u_norm):.4f} mm")

        # 4. Thermal simulation
        print("Running thermal simulation …")
        therm_model = Models.Thermal(k=_COND_K)
        simu_therm  = Thermal(mesh, therm_model, verbosity=False)

        Nn = mesh.Nn
        group_tet = mesh.dict_groupElem[ElemType.TETRA4]
        connect   = group_tet.connect

        mask_fins = np.zeros(Nn, dtype=bool)
        mask_base = np.zeros(Nn, dtype=bool)
        mask_top  = np.zeros(Nn, dtype=bool)
        mask_fins[nodes_fins]  = True
        mask_base[nodes_fixed] = True
        mask_top[nodes_top]    = True
        mask_fins &= ~mask_top

        print("  Building Robin BC matrices …")
        M_fins, f_fins = _build_robin_matrices(coords, connect, mask_fins, Nn)
        M_base, f_base = _build_robin_matrices(coords, connect, mask_base, Nn)

        K_cond, _, _, F_src = simu_therm.Get_K_C_M_F()
        K_total = (K_cond
                   + self.h_conv_fins * M_fins
                   + self.h_conv_base * M_base)
        F_total = (F_src
                   + self.h_conv_fins * self.T_ambient  * sp.csr_matrix(f_fins.reshape(-1, 1))
                   + self.h_conv_base * self.T_base_hot * sp.csr_matrix(f_base.reshape(-1, 1)))
        F_vec = np.asarray(F_total.todense()).flatten()

        print("  Solving …")
        T_field = spla.spsolve(K_total.tocsr(), F_vec)
        simu_therm._Set_solutions(ModelType.thermal, T_field)

        print(f"  Min temperature : {np.min(T_field):.2f} K")
        print(f"  Max temperature : {np.max(T_field):.2f} K")

        return HeatSinkResults(
            max_stress=float(np.max(svm)),
            max_displacement=float(np.max(u_norm)),
            min_temperature=float(np.min(T_field)),
            max_temperature=float(np.max(T_field)),
            delta_T=float(np.max(T_field) - self.T_ambient),
            _simu_mech=simu_mech,
            _simu_therm=simu_therm,
            _svm=svm,
            _u_norm=u_norm,
            _T_field=T_field,
            _params=dict(
                mech_load_pressure=self.mech_load_pressure,
                h_conv_fins=self.h_conv_fins,
                h_conv_base=self.h_conv_base,
                T_ambient=self.T_ambient,
                T_base_hot=self.T_base_hot,
            ),
        )

    # ------------------------------------------------------------------
    def plot(self, results: HeatSinkResults, save: bool = True) -> None:
        """Open an interactive 3D plot of the FEA results.

        Parameters
        ----------
        results : HeatSinkResults
            Output from a previous ``__call__`` or ``_run``.
        save : bool
            If True, also save a PNG to data/CAD_generated/.
        """
        p = results._params
        fig = plt.figure(figsize=(14, 10))

        ax_svm  = fig.add_subplot(2, 2, 1, projection="3d")
        ax_disp = fig.add_subplot(2, 2, 2, projection="3d")
        ax_temp = fig.add_subplot(2, 2, 3, projection="3d")

        Display.Plot_Result(results._simu_mech, "Svm",
                            nodeValues=True, plotMesh=False, ax=ax_svm)
        ax_svm.set_title(f"von Mises stress  (max = {results.max_stress:.2f} N/mm²)")

        Display.Plot_Result(results._simu_mech, "displacement_norm",
                            nodeValues=True, plotMesh=False, ax=ax_disp)
        ax_disp.set_title(f"Displacement norm  (max = {results.max_displacement:.4f} mm)")

        Display.Plot_Result(results._simu_therm, "thermal",
                            nodeValues=True, plotMesh=False, ax=ax_temp)
        ax_temp.set_title(
            f"Temperature  (min={results.min_temperature:.1f} K,"
            f" max={results.max_temperature:.1f} K)"
        )

        ax_txt = fig.add_subplot(2, 2, 4)
        ax_txt.axis("off")
        summary = (
            "Optimization metrics\n"
            "─────────────────────────────────\n"
            f"Max von Mises stress : {results.max_stress:.2f} N/mm²\n"
            f"Max displacement     : {results.max_displacement:.4f} mm\n"
            f"Max temperature      : {results.max_temperature:.2f} K\n"
            f"Min temperature      : {results.min_temperature:.2f} K\n"
            f"ΔT (max - T_amb)     : {results.delta_T:.2f} K\n"
            "\nInput parameters\n"
            "─────────────────────────────────\n"
            f"mech_load_pressure   : {p['mech_load_pressure']:.3g} N/mm²\n"
            f"h_conv_fins          : {p['h_conv_fins']:.2e} W/(mm²·K)\n"
            f"h_conv_base          : {p['h_conv_base']:.2e} W/(mm²·K)\n"
            f"T_ambient            : {p['T_ambient']:.1f} K\n"
            f"T_base_hot           : {p['T_base_hot']:.1f} K\n"
        )
        ax_txt.text(0.05, 0.95, summary, transform=ax_txt.transAxes,
                    verticalalignment="top", fontfamily="monospace", fontsize=9)

        plt.tight_layout()

        if save:
            out_path = ROOT / "data" / "CAD_generated" / "heat_sink_FEA_results.png"
            plt.savefig(out_path, dpi=150, bbox_inches="tight")
            print(f"Plot saved to: {out_path}")

        plt.show()


# ---------------------------------------------------------------------------
# Script entry point
# ---------------------------------------------------------------------------
if __name__ == "__main__":
    model   = HeatSinkFEA()
    results = model(plot=True)
    print(results)
