"""FEA simulation of the fin-array heat sink: linear elastic + thermal diffusion.

Geometry : data/CAD_generated/heat_sink.step  (generated by CAD_heatSink.py)
Models   : Isotropic linear elastic (stress/displacement)
           Thermal diffusion with Robin convection BCs (temperature)

Face names in the STEP file and how they map to BCs
-----------------------------------------------------
mech_fixed_base  – base bottom (z=0)        → mechanical clamped + thermal base convection
mech_load_top    – fin tops (z=z_total)     → applied surface pressure
therm_conv_fins  – fin sides + channel floor → fin convection (cooling side)
end_cap          – x-direction end faces    → no BC (adiabatic symmetry)

See CAD_heatSink.py for the face-classification logic.

Optimization parameters (marked PARAM below):
    mech_load_pressure  – pressure on fin tops [N/mm²]
    h_conv_fins         – convection coefficient on fin surfaces [W/(mm²·K)]
    h_conv_base         – convection coefficient on base [W/(mm²·K)]
    T_ambient           – ambient temperature (fin cooling side) [K]
    T_base_hot          – hot-side temperature at base [K]

Units
-----
All lengths in mm.  Forces in N.  Stress in N/mm² (= MPa).
Thermal conductivity in W/(mm·K).  Heat-transfer coeff in W/(mm²·K).
"""

from __future__ import annotations

import shutil
import sys
from pathlib import Path

import matplotlib
matplotlib.use("TkAgg")
import matplotlib.pyplot as plt
import numpy as np
import scipy.sparse as sp
import scipy.sparse.linalg as spla

ROOT = Path(__file__).resolve().parents[2]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

from EasyFEA import Display, ElemType, MatrixType, Mesher
from EasyFEA import Models
from EasyFEA.Simulations import Elastic, Thermal

# ---------------------------------------------------------------------------
# STEP file paths
# EasyFEA's Mesh_Import_part requires the extension ".stp" (checks for ".stp"
# in the filename string). We keep the canonical .step file and copy it.
# ---------------------------------------------------------------------------
STEP_FILE = ROOT / "data" / "CAD_generated" / "heat_sink.step"
STP_FILE  = STEP_FILE.with_suffix(".stp")

# ---------------------------------------------------------------------------
# Geometry constants  (must match the defaults in CAD_heatSink.py)
# ---------------------------------------------------------------------------
FIN_HEIGHT    = 20.0   # mm
BASE_HEIGHT   =  5.0   # mm

Z_BASE  = BASE_HEIGHT                  # channel floor z-coordinate
Z_TOTAL = BASE_HEIGHT + FIN_HEIGHT     # fin-top z-coordinate

# ---------------------------------------------------------------------------
# Material properties — Aluminium (N, mm, K unit system)
# ---------------------------------------------------------------------------
DENSITY  = 2.70e-3    # kg/mm³   (= 2700 kg/m³)
YOUNG_E  = 70_000.0   # N/mm²    (= 70 GPa)
POISSON  = 0.33       # –
COND_K   = 0.200      # W/(mm·K) (= 200 W/(m·K))

# ---------------------------------------------------------------------------
# OPTIMIZATION PARAMETERS
# ---------------------------------------------------------------------------
# Mechanical
mech_load_pressure: float = -1.0        # PARAM [N/mm²]  negative → compressive

# Thermal – convection coefficients on the two BC surfaces
h_conv_fins: float = 5.0e-5             # PARAM [W/(mm²·K)]  ≈ 50 W/(m²·K)
h_conv_base: float = 1.0e-4             # PARAM [W/(mm²·K)]  ≈ 100 W/(m²·K)

# Thermal – reference temperatures for each Robin BC
T_ambient:   float = 300.0              # PARAM [K]  ambient (fin cooling side)
T_base_hot:  float = 350.0              # PARAM [K]  hot-side temperature at base

# ---------------------------------------------------------------------------
# Meshing options
# ---------------------------------------------------------------------------
ELEM_TYPE  = ElemType.TETRA4   # TETRA4 (linear) or TETRA10 (quadratic)
MESH_SIZE  = 3.0               # target element size [mm]
COORD_TOL  = 0.5               # tolerance for surface node selection [mm]

# ===========================================================================
# 1. MESH
# ===========================================================================
print("Meshing …")
shutil.copy(STEP_FILE, STP_FILE)

mesher = Mesher()
mesh = mesher.Mesh_Import_part(str(STP_FILE), dim=3, meshSize=MESH_SIZE, elemType=ELEM_TYPE)

print(f"  Nodes: {mesh.Nn}   Elements: {mesh.Ne}")

coords = mesh.coord          # (Nn, 3)

# ===========================================================================
# 2. NODE SETS  (coordinate-based, matching CAD_heatSink.py face definitions)
# ===========================================================================
tol = COORD_TOL

# mech_fixed_base / therm_conv_base  — base bottom at z ≈ 0
nodes_fixed = mesh.Nodes_Conditions(lambda x, y, z: z <= tol)

# mech_load_top  — fin tops at z ≈ Z_TOTAL
nodes_top = mesh.Nodes_Conditions(lambda x, y, z: z >= Z_TOTAL - tol)

# therm_conv_fins — fin side faces + channel floors
# Channel floors sit at z ≈ Z_BASE.
# Fin side faces are y-normal faces above z = Z_BASE; they can be selected as
# all surface nodes with z > Z_BASE that are not at the very top (to exclude
# mech_load_top overlap).  In practice a small y-tolerance is sufficient but
# we use the full z-range above the base to be inclusive.
nodes_fins = mesh.Nodes_Conditions(
    lambda x, y, z: (np.abs(z - Z_BASE) < tol) | (z > Z_BASE - tol)
)

# ===========================================================================
# 3. MECHANICAL SIMULATION — isotropic linear elastic
# ===========================================================================
print("Running mechanical simulation …")

mech_model = Models.Elastic.Isotropic(dim=3, E=YOUNG_E, v=POISSON)
simu_mech  = Elastic(mesh, mech_model, verbosity=False)

# Clamped base: u_x = u_y = u_z = 0
simu_mech.add_dirichlet(nodes_fixed, [0, 0, 0], ["x", "y", "z"])

# Pressure on fin tops (normal = +z, so z-direction load)
simu_mech.add_surfLoad(nodes_top, [mech_load_pressure], ["z"])

u_mech = simu_mech.Solve()

u_x = simu_mech.Result("ux", nodeValues=True)
u_y = simu_mech.Result("uy", nodeValues=True)
u_z = simu_mech.Result("uz", nodeValues=True)
u_norm = np.sqrt(u_x**2 + u_y**2 + u_z**2)
svm    = simu_mech.Result("Svm", nodeValues=True)

print(f"  Max von Mises stress : {np.max(svm):.3f} N/mm²")
print(f"  Max displacement     : {np.max(u_norm):.4f} mm")

# ===========================================================================
# 4. THERMAL SIMULATION — steady-state diffusion with Robin (convection) BCs
#
# Strong form:  -k ∇²T = 0   in Ω
# Robin BC:     k ∂T/∂n = h (T - T_ref)   on ∂Ω_conv
#
# Weak form adds to the system:
#   K_total = K_cond  +  Σ_i  h_i * M_surf_i
#   F_total =           Σ_i  h_i * T_ref_i * f_surf_i
#
# M_surf = ∫ N' N dS   (surface mass matrix on the Robin boundary)
# f_surf = ∫ N   dS    (surface load vector  on the Robin boundary)
#
# We build M_surf and f_surf by extracting the triangular boundary faces
# (TRI3) from the TETRA4 connectivity and integrating over those that lie
# on each named surface.
# ===========================================================================
print("Running thermal simulation …")

therm_model = Models.Thermal(k=COND_K)
simu_therm  = Thermal(mesh, therm_model, verbosity=False)


def _build_robin_matrices(
    coords: np.ndarray,
    connect: np.ndarray,
    node_mask: np.ndarray,
    Nn: int,
) -> tuple[sp.csr_matrix, np.ndarray]:
    """Build the sparse surface mass matrix and load vector for a Robin BC.

    Parameters
    ----------
    coords  : (Nn, 3) node coordinates
    connect : (Ne, 4) TETRA4 connectivity (0-based node indices)
    node_mask : boolean array of length Nn; True for nodes on the BC surface
    Nn      : total number of nodes

    Returns
    -------
    M_surf : (Nn, Nn) sparse CSR surface mass matrix  ∫ N' N dS
    f_surf : (Nn,)   surface load vector              ∫ N dS
    """
    # Extract all faces of all tetrahedra.
    # Each tet has 4 faces defined by 3 of its 4 local nodes.
    face_local = np.array([[0, 1, 2], [0, 1, 3], [0, 2, 3], [1, 2, 3]])

    all_faces = connect[:, face_local]   # (Ne, 4, 3)
    all_faces = all_faces.reshape(-1, 3) # (4*Ne, 3)
    all_faces_sorted = np.sort(all_faces, axis=1)

    # Boundary faces appear exactly once in the face list
    face_keys = all_faces_sorted[:, 0] * (Nn**2) + \
                all_faces_sorted[:, 1] * Nn + \
                all_faces_sorted[:, 2]
    _, counts = np.unique(face_keys, return_counts=True)
    unique_keys, inv = np.unique(face_keys, return_inverse=True)
    boundary_face_ids = np.where(counts == 1)[0]

    # Map back from unique index to original face index
    boundary_mask_per_face = np.isin(inv, boundary_face_ids)   # (4*Ne,)
    boundary_faces = all_faces[boundary_mask_per_face]          # (Nbf, 3)

    # Keep only faces where ALL three nodes are on the target surface
    on_surface = node_mask[boundary_faces]    # (Nbf, 3) bool
    face_on_surf = on_surface.all(axis=1)
    surf_faces = boundary_faces[face_on_surf] # (Nf, 3)

    if surf_faces.shape[0] == 0:
        return sp.csr_matrix((Nn, Nn)), np.zeros(Nn)

    # Integrate over each triangular face.
    # For TRI3: M_e[i,j] = area/12 * (1 + delta_ij)
    # i.e. diagonal = area/6, off-diagonal = area/12
    p0 = coords[surf_faces[:, 0]]
    p1 = coords[surf_faces[:, 1]]
    p2 = coords[surf_faces[:, 2]]
    cross = np.cross(p1 - p0, p2 - p0)
    areas = 0.5 * np.linalg.norm(cross, axis=1)   # (Nf,)

    # Assemble sparse M_surf using COO format
    rows, cols, vals = [], [], []
    for local_i in range(3):
        for local_j in range(3):
            gi = surf_faces[:, local_i]
            gj = surf_faces[:, local_j]
            coef = areas / 12.0 * (2.0 if local_i == local_j else 1.0)
            rows.append(gi)
            cols.append(gj)
            vals.append(coef)

    rows = np.concatenate(rows)
    cols = np.concatenate(cols)
    vals = np.concatenate(vals)
    M_surf = sp.csr_matrix((vals, (rows, cols)), shape=(Nn, Nn))

    # f_surf[i] = ∫ N_i dS = area/3 for each node in each face
    f_surf = np.zeros(Nn)
    for local_i in range(3):
        np.add.at(f_surf, surf_faces[:, local_i], areas / 3.0)

    return M_surf, f_surf


# Get TETRA4 connectivity (0-based)
group_tet = mesh.dict_groupElem[ElemType.TETRA4]
connect   = group_tet.connect   # (Ne, 4) 0-based node indices

Nn = mesh.Nn

# Node masks for each Robin surface
mask_fins = np.zeros(Nn, dtype=bool)
mask_base = np.zeros(Nn, dtype=bool)
mask_fins[nodes_fins]  = True
mask_base[nodes_fixed] = True

# Remove fin-top nodes from fins mask to avoid contaminating the top surface
mask_top = np.zeros(Nn, dtype=bool)
mask_top[nodes_top] = True
mask_fins &= ~mask_top

# Build Robin matrices for each BC surface
print("  Building Robin BC matrices …")
M_fins, f_fins = _build_robin_matrices(coords, connect, mask_fins, Nn)
M_base, f_base = _build_robin_matrices(coords, connect, mask_base, Nn)

# Assemble total thermal system
K_cond, _, _, F_src = simu_therm.Get_K_C_M_F()

K_total = K_cond + h_conv_fins * M_fins + h_conv_base * M_base
F_total = F_src  + h_conv_fins * T_ambient  * sp.csr_matrix(f_fins.reshape(-1, 1)) \
                 + h_conv_base * T_base_hot  * sp.csr_matrix(f_base.reshape(-1, 1))

F_vec = np.asarray(F_total.todense()).flatten()

# Solve directly (no Dirichlet BCs — Robin BCs make the system non-singular)
print("  Solving …")
T_field = spla.spsolve(K_total.tocsr(), F_vec)

# Write solution back so EasyFEA Display methods can access it
from EasyFEA.Models._utils import ModelType
simu_therm._Set_solutions(ModelType.thermal, T_field)

print(f"  Min temperature : {np.min(T_field):.2f} K")
print(f"  Max temperature : {np.max(T_field):.2f} K")

# ===========================================================================
# 5. RESULTS & PLOTS
# ===========================================================================
fig = plt.figure(figsize=(14, 10))
ax_svm  = fig.add_subplot(2, 2, 1, projection="3d")
ax_disp = fig.add_subplot(2, 2, 2, projection="3d")
ax_temp = fig.add_subplot(2, 2, 3, projection="3d")

# --- von Mises stress ---
ax = ax_svm
Display.Plot_Result(
    simu_mech, "Svm",
    nodeValues=True,
    plotMesh=False,
    ax=ax,
    title="von Mises stress [N/mm²]",
)
max_svm = np.max(svm)
ax.set_title(f"von Mises stress  (max = {max_svm:.2f} N/mm²)")

# --- Displacement norm ---
ax = ax_disp
Display.Plot_Result(
    simu_mech, "displacement_norm",
    nodeValues=True,
    plotMesh=False,
    ax=ax,
    title="Displacement [mm]",
)
max_disp = np.max(u_norm)
ax.set_title(f"Displacement norm  (max = {max_disp:.4f} mm)")

# --- Temperature field ---
ax = ax_temp
Display.Plot_Result(
    simu_therm, "thermal",
    nodeValues=True,
    plotMesh=False,
    ax=ax,
    title="Temperature [K]",
)
ax.set_title(f"Temperature  (min={np.min(T_field):.1f} K, max={np.max(T_field):.1f} K)")

# --- Optimization metrics summary ---
ax = fig.add_subplot(2, 2, 4)
ax.axis("off")
summary = (
    "Optimization metrics\n"
    "─────────────────────────────────\n"
    f"Max von Mises stress : {max_svm:.2f} N/mm²\n"
    f"Max displacement     : {max_disp:.4f} mm\n"
    f"Max temperature      : {np.max(T_field):.2f} K\n"
    f"Min temperature      : {np.min(T_field):.2f} K\n"
    f"ΔT (max - T_amb)     : {np.max(T_field) - T_ambient:.2f} K\n"
    "\nInput parameters\n"
    "─────────────────────────────────\n"
    f"mech_load_pressure   : {mech_load_pressure:.3g} N/mm²\n"
    f"h_conv_fins          : {h_conv_fins:.2e} W/(mm²·K)\n"
    f"h_conv_base          : {h_conv_base:.2e} W/(mm²·K)\n"
    f"T_ambient            : {T_ambient:.1f} K\n"
    f"T_base_hot           : {T_base_hot:.1f} K\n"
)
ax.text(0.05, 0.95, summary, transform=ax.transAxes,
        verticalalignment="top", fontfamily="monospace", fontsize=9)

plt.tight_layout()
out_path = ROOT / "data" / "CAD_generated" / "heat_sink_FEA_results.png"
plt.savefig(out_path, dpi=150, bbox_inches="tight")
print(f"\nPlot saved to: {out_path}")
plt.show()
